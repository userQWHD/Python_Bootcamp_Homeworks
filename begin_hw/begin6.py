


#Sorag: Parallelepipediň uzynlygy, ini, beýikligi berlen, Şolar bilen parallelepipediň göwrümini we ýerüsti meýdanyny tapyň.

a = float(input("Parallelepipediň uzynlygyny giriziň: "))
b = float(input("Parallelepipediň inini giriziň: "))
c = float(input("Parallelepipediň beýikligini giriziň: "))

V = a * b * c
S = 2 * (a * b + b * c + a * c)

print("Parallelepipediň göwrümi: ", V)
print("Parallelepipediň ýerüsti meýdany: ", S)

