R1 = float(input("Give a number for 'r1' (remember: r1 > r2) :  "))
R2 = float(input("Give a number for 'r2' (remember: r1 > r2) :  "))
pi = 3.14

S1 = pi * (R1)**2
S2 = pi * (R2)**2
S3 = S1 - S2

print("S1 area's value is : ", S1)
print("S2 area's value is : ", S2)
print("S3 area's value is : ", S3)
