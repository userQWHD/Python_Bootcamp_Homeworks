#Sorag: Iki sany 0-syz san berlen, sanlaryň kwadratlarynyň jemini, tapawudyny, önümini, we paýyny tapyň.

a = float(input("birinji 0-syz sanyňyzy giriziň: "))
b = float(input("ikinji 0-syz sanyňyzy giriziň: "))

kw_jemi = a**2 + b**2
kw_tapawudy = a**2 - b**2
kw_önümi = a**2 * b**2
kw_paýy = a**2 / b**2

print("Kwadratyň jemi: ", kw_jemi)
print("Kwadratyň tapawudy: ", kw_tapawudy)
print("Kwadratyň önümi: ", kw_önümi)
print("Kwadratyň paýy: ", kw_paýy)
